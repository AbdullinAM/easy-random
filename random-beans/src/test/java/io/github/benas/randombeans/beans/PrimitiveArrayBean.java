package io.github.benas.randombeans.beans;

/**
 * Java bean used to test the population of collection classes.
 *
 * @author Fred Eckertson (feckertson@cerner.com)
 */
public class PrimitiveArrayBean {
    byte[] byteArray;
    short[] shortArray;
    int[] intArray;
    long[] longArray;
    float[] floatArray;
    double[] doubleArray;
    boolean[] booleanArray;
    char[] charArray;

    public byte[] getByteArray() {
        return byteArray;
    }
    public short[] getShortArray() {
        return shortArray;
    }
    public int[] getIntArray() {
        return intArray;
    }
    public long[] getLongArray() {
        return longArray;
    }
    public float[] getFloatArray() {
        return floatArray;
    }
    public double[] getDoubleArray() {
        return doubleArray;
    }
    public char[] getCharArray() {
        return charArray;
    }
    public boolean[] getBooleanArray() {
        return booleanArray;
    }
}
